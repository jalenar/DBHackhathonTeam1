package com.certifiedstudent.model;

public class CStudent {

	private String imageUrl;
	
	private String interests;
	
	private int overallScore;
	
	private String contact;
	
	private String endorsements;
	
	private String careerAdvice;

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public String getInterests() {
		return interests;
	}

	public void setInterests(String interests) {
		this.interests = interests;
	}

	public int getOverallScore() {
		return overallScore;
	}

	public void setOverallScore(int overallScore) {
		this.overallScore = overallScore;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public String getEndorsements() {
		return endorsements;
	}

	public void setEndorsements(String endorsements) {
		this.endorsements = endorsements;
	}

	public String getCareerAdvice() {
		return careerAdvice;
	}

	public void setCareerAdvice(String careerAdvice) {
		this.careerAdvice = careerAdvice;
	}
	
}
